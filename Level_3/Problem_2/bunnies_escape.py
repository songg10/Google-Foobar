def solution(map):
    # Source node represented as (x, y, #_obstacles_removal, distance to source)
    # Distance is 1 because we are
    # counting the source node in the path
    source = (0, 0, 1, 1)
    
    # Escape cell
    escape_x = len(map[0]) - 1
    escape_y = len(map) - 1
    
    # Queue to store the all the neighbor cells
    q = []
    q.append(source)

    # Movement vector in order: Right, down, left, up
    move_vector = [(1, 0), (0, 1), (-1, 0), (0, -1)]
    
    # A list to keep track of visited node
    visited = set()

    while len(q) > 0:
        x, y, r, dist = q.pop(0)

        if x == escape_x and y == escape_y:
            return dist

        if (x, y, r) in visited:
            continue
        visited.add((x, y, r))

        if map[y][x] == 1:
            # If the current cell is an obstacle and we still have 
            # a remaining obstacle removal, then remove it
            if r > 0:
                r -= 1
            else:
                continue

        for dx, dy in move_vector:
            if ((x + dx <= escape_x and x + dx >= 0)
                and (y + dy <= escape_y and y + dy >= 0)):
                q.append((x + dx, y + dy, r, dist + 1))



